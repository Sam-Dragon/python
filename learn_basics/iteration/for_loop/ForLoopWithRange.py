# i = 1, i < 10
print("\nAll numbers from 1 to 10")
for number in range(1, 10):
    print(number)

print("\nOdd numbers from 1 to 10")
# i = 1, i < 10, i + 2
for number in range(1, 10, 2):
    print(number)


print("\nEven numbers from 1 to 10")
# i = 1, i < 10, i + 2
for number in range(2, 10, 2):
    print(number)
